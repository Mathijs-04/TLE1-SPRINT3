    <h1 class="aboutushead1">over EXPCorp.</h1>
    <p class="aboutustext">Welkom bij EXPCorp, waar we de kracht van virtual reality inzetten om het leven van senioren te verrijken. Ons doel is om ouderen in verzorgingstehuizen een wereld van nieuwe mogelijkheden te bieden, door ze mee te nemen op avonturen die verder gaan dan de muren van hun dagelijkse omgeving. Bij EXPCorp geloven we dat leeftijd geen beperking mag zijn om de wereld te blijven ontdekken en bijzondere momenten te ervaren.</p>
    <h2 class="aboutushead">Wat We Doen</h2>
    <p class="aboutustext">Bij EXPCorp ontwikkelen en bieden we een breed scala aan virtual reality-ervaringen, speciaal ontworpen om toegankelijk, veilig en plezierig te zijn voor ouderen. Of het nu gaat om een ontspannende wandeling door een prachtig bos, het bijwonen van een klassiek concert of het beleven van een sportwedstrijd alsof je er zelf bij bent, wij zorgen ervoor dat elke ervaring onvergetelijk is. Ons doel is om ouderen de kans te geven om opnieuw verbinding te maken met passies, herinneringen op te halen en nieuwe avonturen te beleven – allemaal vanuit het comfort van hun eigen stoel.</p>
    <h2 class="aboutushead">Onze Missie</h2>
    <p class="aboutustext">Wij zijn gepassioneerd over het verbeteren van de kwaliteit van leven van ouderen. Door middel van virtual reality willen we de eenzaamheid doorbreken, verveling tegengaan en het mentale welzijn van bewoners in verzorgingshuizen ondersteunen. EXPCorp biedt niet alleen entertainment, maar ook een bron van vreugde, interactie en verbondenheid met de buitenwereld.</p>
    <h2 class="aboutushead">Op Maat Gemaakte Ervaringen</h2>
    <p class="aboutustext">Elke persoon is uniek, en daarom geloven we in gepersonaliseerde VR-ervaringen. Ons team werkt nauw samen met verzorgingstehuizen om ervaringen te creëren die aansluiten bij de interesses en voorkeuren van bewoners. Van rustige wandelingen in het park tot het herbeleven van verre vakanties of het ontdekken van nieuwe culturen – de mogelijkheden zijn eindeloos.</p>
    <h2 class="aboutushead">Waarom EXPCorp?</h2>
    <p class="aboutustext">Met onze jarenlange ervaring op het gebied van virtual reality en ouderenzorg, weten we precies hoe we VR op een veilige en comfortabele manier kunnen inzetten. Onze technologie is eenvoudig te gebruiken en speciaal ontwikkeld voor senioren. We hechten veel waarde aan veiligheid en comfort, zodat iedereen zorgeloos kan genieten van hun virtuele avontuur.
        Bij EXPCorp draait het om het creëren van herinneringen en het openen van nieuwe werelden. Stap in een virtuele realiteit waar alles mogelijk is!
    </p>
